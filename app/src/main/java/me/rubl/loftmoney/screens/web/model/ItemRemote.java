package me.rubl.loftmoney.screens.web.model;

public class ItemRemote {

    private String id;
    private String name;
    private Integer price;
    private String type;
    private String date;

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public int getPrice() {
        return price;
    }

    public String getType() {
        return type;
    }

    public String getDate() {
        return date;
    }
}
